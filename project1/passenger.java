package project1;

public class passenger {
	    private String name;
	    private String contactInfo;

	    public passenger(String name, String contactInfo) {
	        this.name = name;
	        this.contactInfo = contactInfo;
	    }

	    public String getName() {
	        return name;
	    }

	    public String getContactInfo() {
	        return contactInfo;
	    }
	}


